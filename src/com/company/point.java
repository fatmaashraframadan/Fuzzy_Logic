package com.company;

public class point {
    Double x;
    Double y;

    point(Double x, Double y){
        this.x = x;
        this.y = y;
    }

    public Double getY() {
        return y;
    }
    public void setY(Double y) {
        this.y = y;
    }

    public Double getX() {
        return x;
    }
    public void setX(Double x) {
        this.x = x;
    }
}
